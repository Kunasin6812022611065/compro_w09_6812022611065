#include <stdio.h>          // นำเข้าไลบรารีมาตรฐาน สำหรับฟังก์ชัน printf

int main() {                // ฟังก์ชันหลักของโปรแกรม จุดเริ่มต้นเมื่อรัน
    int i = 1;              // ประกาศตัวแปรจำนวนเต็ม i และกำหนดค่าเริ่มต้นเป็น 1

    while (i <= 10) {       // ลูป while ทำงานต่อเมื่อ i น้อยกว่าหรือเท่ากับ 10
        if (i == 6) {       // ตรวจสอบว่า i เท่ากับ 6 หรือไม่
            i++;            // เพิ่มค่า i ก่อนใช้ continue เพื่อไม่ให้เกิดลูปไม่รู้จบ
            continue;       // ข้ามคำสั่งที่เหลือในรอบนี้ แล้วกลับไปตรวจเงื่อนไขลูป
        }
        printf("%d ", i);   // แสดงค่าปัจจุบันของ i พร้อมเว้นวรรค
        i++;                // เพิ่มค่า i ทีละ 1 หลังจากแสดงผล
    }

    return 0;               // คืนค่า 0 เพื่อบอกว่าโปรแกรมทำงานสำเร็จสมบูรณ์
}
